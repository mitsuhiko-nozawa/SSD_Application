def seed_everything(seed=42):
    random.seed(seed)
    os.environ['PYTHONHASHSEED'] = str(seed)
    np.random.seed(seed)
    torch.manual_seed(0)
    os.environ["CUDA_LAUNCH_BLOCKING"] = "1"
